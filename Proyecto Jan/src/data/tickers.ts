export function getTickerMeta(ticker: string): TickerMeta {
  const normalized = ticker.trim().toUpperCase();
  return TICKERS.find((item) => item.ticker === normalized) ?? {
    ...UNKNOWN_TICKER,
    ticker: normalized || "UNKNOWN",
    name: normalized ? `${normalized} (Unknown)` : UNKNOWN_TICKER.name,
  };
}

